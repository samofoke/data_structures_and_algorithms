function Binary_Search(arr, v) {

    let i = 0;
    let r = arr.length - 1;
    let d = 0; 

    while (i <= r) {
        d = Math.floor((i + r) /2);
        if (arr[d] == v) {
            return d;
        } else if (arr[d] < v) {
            i = d + 1;
        } else {
            r = d - 1;
        }
    }
    return "nothing found";
}

